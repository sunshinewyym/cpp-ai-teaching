function plain(value) {
  return String(value || '').replace(/[`$]/g, '').replace(/\\text\{([^}]*)\}/g, '$1').trim();
}

function choiceTip(question) {
  const text = question.question || '';
  if (/二进制|八进制|十六进制|位|字节|MB|KB/.test(text)) return '先统一单位或进制，按位权写展开式；位运算务必右对齐后逐位计算。';
  if (/排列|组合|选出|分配|方案|花色|手套|球/.test(text)) return '先判断对象是否相同、顺序是否重要，再选择排列、组合、隔板法或反面计数。';
  if (/二叉树|三叉树|遍历|哈夫曼|图|栈|队列|链表/.test(text)) return '画一个最小结构或按操作顺序模拟，重点检查访问顺序、下标关系和数据结构的不变量。';
  if (/复杂度|查找|排序|递归|算法/.test(text)) return '先确定一次基本操作，再数循环层数、递归规模或每轮缩小比例，不要只凭算法名称判断。';
  return '抓住题干中的定义关键词，先独立写出结论，再与选项逐项核对，避免被相近概念干扰。';
}

export function buildLegacyChoiceExplanation(question) {
  const reason = choiceReasons[question.id] || `正确选项符合题干定义与条件，代入或逐项核对可得到 ${plain(question.options?.[question.answer])}。`;
  const summary = question.number === 15 ? '\n\n**本年选择题整体方法：** 基础概念题抓定义中的限定词；进制与位运算先统一表示；排列组合先判断对象是否相同及顺序是否重要；栈、树、图题画最小结构模拟；算法题先数基本操作。计算后务必用选项反查数量级和边界。' : '';
  return `参考答案为 ${question.answer}（${plain(question.options?.[question.answer])}）。\n\n**详细解析：**\n\n${reason}\n\n**解题技巧：** ${choiceTip(question)}\n\n**易错点：** 不要只凭选项外观作答；计算题完成后代回原条件，概念题要检查题干中的“至少、至多、不正确”等限定词。${summary}`;
}

function programQuestionTip(question, problem) {
  const text = question.text || '';
  if (problem.type === 'completion') return '把正确选项代回完整语句，同时检查类型、边界、循环推进和该空前后变量的含义是否一致。';
  if (/时间复杂度|复杂度/.test(text)) return '只统计主导操作的执行次数，并分别分析最好、最坏或递归分支，忽略常数项。';
  if (/删|改|移到|替换|修改/.test(text)) return '比较修改前后是否仍保持原程序的不变量；构造 1～4 个元素的最小反例通常比重算大样例更快。';
  if (/输入|输出|返回/.test(text)) return '只记录会影响答案的变量，用表格逐轮更新；能先化简出公式或周期时，不要机械模拟所有循环。';
  return '先把题干说法翻译成代码条件，再检查边界输入和最小反例，判断“总是”“一定”等强结论是否成立。';
}

