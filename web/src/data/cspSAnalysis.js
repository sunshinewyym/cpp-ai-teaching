function plain(value) {
  return String(value || '').replace(/[`$]/g, '').replace(/\\text\{([^}]*)\}/g, '$1').replace(/\\n/g, '\n').trim();
}

function isMojibake(value) {
  return /鍙|瑙ｆ|锛|銆|绋嬪|閫夐/.test(String(value || ''));
}

function stripOldHeader(value) {
  return plain(value).replace(/^参考答案[\s\S]*?\*\*解析：?\*\*\s*/u, '').trim();
}

function choiceTip(question) {
  const text = question.question || '';
  if (/进制|位运算|字节|MB|GB|哈希/.test(text)) return '统一表示后再算；进制题写位权展开式，位运算逐位对齐，容量题始终标注 bit、Byte、MB 的换算。';
  if (/排列|组合|方案|选取|至少|至多|概率/.test(text)) return '先判断对象是否相同、顺序是否重要，再决定排列、组合、递推或补集计数；最后用极小规模检查是否重复或漏算。';
  if (/树|图|栈|队列|链表|遍历|最短路/.test(text)) return '画最小结构并按操作逐步模拟；树图题同步记录结点、边和访问状态，不凭算法名称直接猜结论。';
  if (/复杂度|排序|递归|算法/.test(text)) return '找出主导操作，数循环次数、递归规模或每轮缩小比例；平均、最坏、上界和精确次数要分别判断。';
  return '圈出“正确/不正确、至少/至多、一定/可能”等限定词，先独立得到结论，再与选项核对。';
}

