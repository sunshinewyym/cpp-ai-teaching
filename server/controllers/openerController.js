const { chat } = require('../services/deepseek');
const { setupSSE, sendSSE, endSSE } = require('../utils/stream');

/**
 * POST /api/opener
 * Body: { courseTopic }
 * Generates an algorithm brainstorm (算法脑洞) for class opener
 * Returns structured JSON: brainstorm + step1~step4
 */
async function handleOpener(req, res) {
  const { courseTopic } = req.body;

  if (!courseTopic) {
    return res.status(400).json({ error: 'courseTopic is required' });
  }

  const prompt = `你是 C++ 竞赛教练 AI，专为"${courseTopic}"生成课堂导入脑洞。

## ⚠️ 铁律限制
1. **严禁输出任何 C++ 代码、伪代码！** 连 for, if, while 等关键字都不允许作为代码块出现。
2. **严禁直接宣告最终算法名称！** 必须通过对题目蛛丝马迹的分析，引导学生自己"顿悟"。
3. 涉及算法复杂度、状态转移方程时，请使用标准的 **LaTeX 数学公式格式**（例如 $O(N \\log N)$ 或 $dp[i]$）。

## 💡 听得懂的语言与自由故事流规范
- **大白话翻译**：禁止使用高深的学术腔调！把"指数级爆炸"换成"数据多到电脑原地冒烟"；把"回溯"换成"发现走错了赶紧退回上一步"。
- **零技术细节**：严禁出现任何代码实现层面的字眼（如 visited数组、递归层数、指针、双亲表示法等具体术语）。
- **无标签纯故事**：去掉所有生硬的标签！直接输出一段生动有趣、画面感极强的微型故事，用于映射算法的核心思想。
- **字数与视觉硬限制**：脑洞故事控制在 **150 字以内**。所有板块采用【短句分点（使用 - 或 *）】，关键矛盾或反差词【**加粗高亮**】。

## 殿堂级中小学语调范例（严格模仿其文风与字数）
题目背景：深度优先搜索（DFS）
{
  "brainstorm": "### 🎬 3分钟脑洞：特工小队的'撞南墙'任务\\n- 你派出了一名智能特工，去探索一座布满大雾、出口未知的**地下神秘迷宫**。\\n- 迷宫岔路太多了！如果特工每到一个路口，都**分身成好几个人**分头去探，很快他就会分身太多导致能量耗尽，原地死机。\\n- 于是特工决定死磕到底：**认准一条路疯狂往前冲，直到一头撞上死胡同，再退回上一个路口换一条路继续冲**。\\n- 这种**'不撞南墙不回头'**的死磕精神，反而用最省力的方法摸清了整座迷宫！这就是今天我们要用代码降服的超级力量。",
  "step1": "- **地毯式搜寻**：题目要求我们把从起点到终点的所有走法全部找出来，一个都不能漏。\\n- **分叉大树**：每走到一个格子，都面临上下左右四个分叉路口，这就像是一棵**长满了分叉的探险树**。\\n- **关键思考**：怎样走路才能**不迷路、不兜圈子**，而且能把每条路都试一遍？",
  "step2": "- **看破数据**：迷宫地图大小 $N \\times M \\le 100$，格子数量不算多。\\n- **原地转圈危机**：如果我们像没头苍蝇一样乱走，或者在原地转圈圈，计算量会变成恐怖的 $O(4^{N \\times M})$，**电脑会直接卡死甚至冒烟**。\\n- **我们的目标**：必须找到一种**按规矩排队走路、绝不走重复冤枉路**的聪明办法。",
  "step3": "- **找超能力工具**：我们需要一个'后进先出'的魔法口袋——**走得最深的那个格子，要是走错了必须最先从口袋里退出来**。\\n- **天然的口袋**：C++ 里的函数自己调用自己时，系统会自动帮我们准备好这个特殊的口袋。我们直接用它来记住特工来时的路。",
  "step4": "- **核心破局点**：特工每到一个新格子，先干两件事：**在地上画个小红旗证明我来过**，然后向四周的邻居探路。\\n- **擦掉脚印**：如果四周都是死胡同，**在往后退回上一步之前，必须把地上的小红旗擦掉**！因为要允许别的路线以后经过这里。\\n- **大功告成**：只要一看到终点的坐标，立刻在心里给总路线加 1，然后大喊一声'搞定'，优雅地转身去试下一条新路！"
}

## 输出要求
严格输出 JSON，不要包含任何 markdown 外壳（不要 \`\`\`json 和 \`\`\`）：
{
  "brainstorm": "课前3分钟算法脑洞故事的极简 Markdown 文本（150字以内，无标签纯故事，零技术术语，适合中小学生听懂，用短句分点 + **加粗高亮**关键矛盾）"
}

请为"${courseTopic}"生成完整输出：`;

  setupSSE(res);

  try {
    const content = await chat([{ role: 'user', content: prompt }], {
      temperature: 0.85,
      max_tokens: 2048,
    });
    sendSSE(res, { content });
    endSSE(res);
  } catch (err) {
    console.error('[Opener Error]', err.message);
    sendSSE(res, { error: err.message });
    endSSE(res);
  }
}

module.exports = { handleOpener };
